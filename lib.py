def plot3d(pixels, axis_labels="RGB"):
    """Plot pixels in 3D."""
    if axis_labels == "HLS":
        colors = np.zeros_like(pixels)
        H = pixels[:, :, 0] / 180.
        L = pixels[:, :, 1] / 255.
        S = pixels[:, :, 2] / 255.
        colors = np.dstack((H, L, S))
        axis_limits=[(0, 180), (0, 255), (0, 255)]
    elif axis_labels == "HSV":
        colors = np.zeros_like(pixels)
        H = pixels[:, :, 0] / 180.
        S = pixels[:, :, 1] / 255.
        V = pixels[:, :, 2] / 255.
        colors = np.dstack((H, S, V))
        axis_limits=[(0, 180), (0, 255), (0, 255)]
    elif axis_labels=="RGB":
        colors = pixels / 255.
        axis_limits=[(0, 255), (0, 255), (0, 255)]
    elif axis_labels=="LUV":
        colors = pixels / 255.
        axis_limits=[(0, 255), (0, 255), (0, 255)]
    elif axis_labels=="YUV":
        colors = pixels / 255.
        axis_limits=[(0, 255), (0, 255), (0, 255)]
    else:
        raise ValueError("color space specifier not supported")
    # Create figure and 3D axes
    fig = plt.figure(figsize=(8, 8))
    ax = Axes3D(fig)

    # Set axis limits
    ax.set_xlim(*axis_limits[0])
    ax.set_ylim(*axis_limits[1])
    ax.set_zlim(*axis_limits[2])

    # Set axis labels and sizes
    ax.tick_params(axis='both', which='major', labelsize=14, pad=8)
    ax.set_xlabel(axis_labels[0], fontsize=16, labelpad=16)
    ax.set_ylabel(axis_labels[1], fontsize=16, labelpad=16)
    ax.set_zlabel(axis_labels[2], fontsize=16, labelpad=16)

    # Plot pixel values with colors given in colors_rgb
    ax.scatter(
        pixels[:, :, 0].ravel(),
        pixels[:, :, 1].ravel(),
        pixels[:, :, 2].ravel(),
        c=colors.reshape((-1, 3)), edgecolors='none')

    return ax  # return Axes3D object for further manipulation

